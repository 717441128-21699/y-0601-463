const makeParts = (names: [string, number, string][], stockMap: Record<string, number>): SparePartItem[] =>
  names.map(([name, qty, unit]) => ({
    name,
    quantity: qty,
    unit,
    available: (stockMap[name] || 10) >= qty,
    stock: stockMap[name] || 10,
  }));

